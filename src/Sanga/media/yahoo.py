import logging

from bs4 import BeautifulSoup

from .base import BaseMediaNewsCrawler
from ..struct import NewsStruct

logger = logging.getLogger(__name__)


class Yahoo(BaseMediaNewsCrawler):
    """Web Crawler for Yahoo News"""

    def getInfo(self, link: str) -> NewsStruct:
        return super().getInfo(link)

    @staticmethod
    def _get_content(
        soup: BeautifulSoup,
    ) -> str:

        content = soup.find("div", class_="caas-body").text
        logger.debug(f"CONTENT:\n {content}\n")
        return content
